<p>
	Der Verein New Graphic Standard engagiert sich für eine umweltbewusste Gestaltung und Produktion
	im Kommunikationsdesign und erforscht zeitgemässe, ressourcenschonende Alternativen.
</p>

<p>
	Sowohl in Ausbildung als auch Berufspraxis fehlt es bislang an Bewusstsein, Know-how und
	geeigneten Weiterbildungsangeboten für einen nachhaltigen Umgang mit Printmedien und digitalen
	Alternativen. Auch einschlägige Fachliteratur ist nach wie vor rar.
</p>

<p>
	Um diese Lücken zu schliessen, erarbeitet der Verein fundiertes Fachwissen und bereitet es
	zielgruppengerecht und praxisnah auf. Seit der Gründung im Jahr 2020 durch Ladina Ingold und
	Katharina Scheller widmet sich der Verein der systematischen Analyse gängiger Materialien und
	Produktionsprozesse sowie der Entwicklung nachhaltiger Alternativen.
</p>

<p>
	Ziel ist es, dieses Know-how in die grafische Designpraxis zurückfliessen zu lassen und so die
	Hand­lungskompetenz der Branche in Bezug auf umweltschonende Produktionsprozesse gezielt zu
	stärken. Zu diesem Zweck vernetzt sich der Verein mit Akteur:innen aus Praxis, Berufsverbänden und
	Bildungsinstitutionen. So sind beispielsweise Kooperationen mit der Gewerkschaft Syndicom sowie
	dem Verein Ecopaper entstanden.
</p>

<p>
	Darüber hinaus bringt der Verein seine Expertise durch Vorträge und Workshops gezielt in
	Kommunikationsagenturen, Branchenkonferenzen sowie in Aus- und Weiterbildungsinstitutionen ein.
</p>
